import Foundation
import UIKit

extension UIColor {
	/// Color set
	///
	/// Main Purple Color - #560C7B
	class var mainPurple: UIColor? {return UIColor(named: "mainPurple")}
	/// Sub Purle Color - #B27FEF
	class var subPurple: UIColor? {return UIColor(named: "subPurple")}
	/// Point Orange Color - #E86C3F
	class var pointOrange: UIColor? {return UIColor(named: "pointOrange")}
	/// Primary Color - #333333
	class var primary: UIColor? {return UIColor(named: "primary")}
	/// Secondary Color - #666666
	class var secondary: UIColor? {return UIColor(named: "secondary")}
	/// Tertiary Color - #B5B5B5
	class var tertiary: UIColor? {return UIColor(named: "tertiary")}
	
}
